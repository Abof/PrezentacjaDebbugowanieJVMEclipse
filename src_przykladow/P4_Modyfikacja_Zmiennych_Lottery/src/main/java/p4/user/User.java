package p4.user;

public class User {
	
	public int id;
	public boolean isCool;
	public boolean isTall;
	public boolean isExcluded;
	public boolean isClever;

	public User(int id, boolean isCool, boolean isTall, boolean isExcluded, boolean isClever) {
		super();
		this.id = id;
		this.isCool = isCool;
		this.isTall = isTall;
		this.isExcluded = isExcluded;
		this.isClever = isClever;
	}
}
